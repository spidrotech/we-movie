    public function findRatingByMovieId(int $movieId)
    {            
        return $this->createQueryBuilder('m')
            ->select('m.rating')
            ->where('m.movieId = :movieId')
            ->setParameter('movieId', $movieId)
            ->orderBy('m.id', 'DESC')         // Order by id in descending order
            ->setMaxResults(1)                // Limit to one result
            ->getQuery()
            ->getOneOrNullResult();
    }
